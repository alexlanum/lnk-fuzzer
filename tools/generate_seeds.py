# ===========================================================================
# Section 2: pylnk3 + raw ExtraData injection.
# pylnk3 builds a base LNK; we splice in raw block bytes immediately before the
# ExtraData terminator. Works because the LNK ends with a 4-byte zero terminator
# we can locate by scanning from the end.
# ===========================================================================
print('=== pylnk3 base + raw ExtraData splices ===')

def splice_extra_data(lnk_bytes, *blocks):
    """Insert raw ExtraData block bytes immediately before the trailing terminator.
    The on-disk ExtraData section ends with a 4-byte zero; we strip it, append our
    blocks, and re-add the terminator."""
    if lnk_bytes.endswith(b'\x00\x00\x00\x00'):
        body = lnk_bytes[:-4]
    else:
        body = lnk_bytes
    return body + b''.join(blocks) + b'\x00\x00\x00\x00'


def make_via_pylnk3(target=r'C:\Windows\notepad.exe', **kw):
    import io
    lnk = pylnk3.for_file(target, **kw)
    buf = io.BytesIO()
    lnk.write(buf)
    return buf.getvalue()


