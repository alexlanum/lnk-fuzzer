def pylnk_save_fs(name, target, *, args=None, work_dir=None,
                  description=None, icon=None, icon_index=0):
    """pylnk3.for_file is a thin wrapper that builds a filesystem shortcut.
    We pass everything optionally so the resulting set of files has different
    LinkFlag profiles."""
    lnk = pylnk3.for_file(target, lnk_name=os.path.join(OUT_DIR, name),
                          arguments=args, work_dir=work_dir,
                          description=description, icon_file=icon,
                          icon_index=icon_index)
    print(f'  wrote {name}')

