def build_lnk(*, link_flags, idlist=None, link_info=None,
              strings=None, extra_blocks=()):
    """Assemble a complete LNK from parts. `strings` is a dict of
    {bit: value} where bit is one of LF_HAS_NAME, LF_HAS_RELATIVE_PATH, etc."""
    out = build_header(link_flags)
    if idlist is not None:
        out += with_idlist_size(idlist)
    if link_info is not None:
        out += link_info
    if strings:
        is_uni = bool(link_flags & LF_IS_UNICODE)
        for bit in (LF_HAS_NAME, LF_HAS_RELATIVE_PATH, LF_HAS_WORKING_DIR,
                    LF_HAS_ARGUMENTS, LF_HAS_ICON_LOCATION):
            if link_flags & bit and bit in strings:
                out += build_string_data(strings[bit], is_uni)
    for b in extra_blocks:
        out += b
    out += b'\x00\x00\x00\x00'  # ExtraData terminator
    return out


